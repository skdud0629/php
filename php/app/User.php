<?php

class User extends Authenticatable
{
    use \Illuminate\Auth\Authenticatable;

    protected $fillable = [
        'name',
        'email',
        'password',
    ];

    protected $hidden = [
        'password',
        'remember_token',
    ];

    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    public function articles()
    {
        return $this->hasMany(\App\Models\Article::class);
    }
}
